<h2>Current Version Minus One</h2>
<p>The web applications developed and maintained by META+Lab are designed and tested to work on the most current versions of major desktop web browsers, minus one version: Mozilla Firefox,
  Google Chrome, Safari. Part of the mantra at META+Lab involves pushing boundaries. The use of such state-of-the-art technology will rely on the newest features and functionality available on browsers.</p>

<h2>Additional Notes</h2>
<p>Using current versions of supported browsers also provides benefits in the following areas.</p>

<h3>Security</h3>
<p>New versions of browsers often include patches to known bugs and security vulnerabilities. As such, outdated browsers are comparitively less secure.</p>

<h3>Accessibility</h3>
<p>New versions of browsers are designed with accessibility in mind from the outset rather than as an afterthought. Outdated browsers might not be as accessible to users.</p>

<h3>Support</h3>
<p>New versions of browsers are typically better-supported by the development team(s). Even when available, support for outdated browsers can be minimal at best.</p>

